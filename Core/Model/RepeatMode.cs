namespace NetAudioPlayer.Core.Model
{
    /// <summary>
    /// Режим повтора списка воспроизведения
    /// </summary>
    public enum RepeatMode
    {
        /// <summary>
        /// Не повторять
        /// </summary>
        NoRepeat,
        /// <summary>
        /// Зациклить один элемент
        /// </summary>
        One,
        /// <summary>
        /// Повторять весь список
        /// </summary>
        All
    }
}